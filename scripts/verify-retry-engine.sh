#!/bin/bash

set -e

ARGUMENT="$1"
PACKAGE_NAME="$2"

source generate-util.sh

FAILED=0

generate_retry_engine_code_for_DB_operations(){
  echo "starting to generate retry engine code for DB operations"
  pushd ../../cmd/retry-engine-generator > /dev/null
  go run main.go "$ARGUMENT" "$PACKAGE_NAME"
  echo "successfully created retry engine code for DB operations"
}

verify_retryEngineWrapper() {
  echo "Verifying checksums for Retry Engine Wrapper ..."

  pushd ../database/$ARGUMENT &> /dev/null

#  generate checksum for the branch code
  generate_retryEngineWrapper_checksums
  mv newChecksumsFile.checksum branchChecksumFile.checksum

#  generate checksum for the code generated by the retry engine
  generate_retry_engine_code_for_DB_operations
  pushd ../../database/$ARGUMENT &> /dev/null
  generate_retryEngineWrapper_checksums

# compare the expected and actual checksums
  if [ ! -r ./branchChecksumFile.checksum ]; then
      echo "Error: branchChecksumFile.checksum is missing or not readable."
      FAILED=1
  elif [ ! -r ./newChecksumsFile.checksum ]; then
      echo "Error: newChecksumsFile.checksum is missing or not readable."
      FAILED=1
  elif ! cmp ./branchChecksumFile.checksum ./newChecksumsFile.checksum; then
      echo "Changes detected in the retryEngineWrapper code."
      FAILED=1
  else
      echo "No changes detected. Retry Engine Wrapper code is up-to-date."
  fi

  rm -f ./newChecksumsFile.checksum
  rm -f ./branchChecksumFile.checksum

  popd &> /dev/null
}

verify_retryEngineWrapper

if [ "$FAILED" -ne "0" ]; then
  echo "Verification failed: Changes detected in one or more components."
  exit 1
else
  echo "Verification successful: No changes detected in any components."
fi
